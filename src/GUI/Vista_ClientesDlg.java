    public void consultarTodos()
    {        
//        if(clienteTemp.obtieneLista()!=null)
//        {
        List<Clientes> results = controlador.findClientesEntities();//clienteTemp.obtieneLista();
        
        DefaultTableModel model = (DefaultTableModel) clienteTbl.getModel();
        int rowCount = model.getRowCount();
        //Remove rows one by one from the end of the table
        for (int i = rowCount - 1; i >= 0; i--)
        {
            model.removeRow(i);
        }
        Object rowData[] = new Object[5];
        for (int i = 0; i < results.size(); i++)
        {
            rowData[0] = results.get(i).getId();
            rowData[1] = results.get(i).getNombre();
            rowData[2] = results.get(i).getDireccion();
            rowData[3] = results.get(i).getRfc();
            rowData[4] = results.get(i).getTelefono();
            model.addRow(rowData);
        }
//        }

    }
