    private void guardarbtnActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_guardarbtnActionPerformed
    {//GEN-HEADEREND:event_guardarbtnActionPerformed
        // TODO add your handling code here:
        
        if (idTxtbox.getText().length() != 0 && nombreTxtbox.getText().length() != 0
                && direccionTxtbox.getText().length() != 0 && rfcTxtbox.getText().length() != 0
                && telefonoTxtbox.getText().length() != 0)
        {
            id = Integer.parseInt(idTxtbox.getText().toUpperCase());
            nombre = nombreTxtbox.getText().toUpperCase();
            direccion = direccionTxtbox.getText().toUpperCase();
            rfc = rfcTxtbox.getText().toUpperCase();
            telefono = telefonoTxtbox.getText().toUpperCase();

            cliente = new Cliente(id, nombre, direccion, rfc, telefono);

            String msj = cliente.inserta();
            if(msj.contains("already exists"))
            {
                System.out.println(" ID ya existe");
            }
            
            limpiarTextbox();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Ingrese todos los datos", "Cliente", JOptionPane.INFORMATION_MESSAGE);
            System.out.println("Ingrese todos los datos!");
        }
        
        consultarTodos();
    }//GEN-LAST:event_guardarbtnActionPerformed

    private boolean comparaCliente()
    {    
        List<Clientes> results = controlador.findClientesEntities();
        
        for (int i = 0; i < results.size(); i++)
        {
            if(results.get(i).getNombre()==nombreTxtbox.getText() &&
                    results.get(i).getDireccion()==direccionTxtbox.getText() &&
                    results.get(i).getRfc()==rfcTxtbox.getText() &&
                    results.get(i).getTelefono()==telefonoTxtbox.getText())
            {
                return true;
            }            
        }
        return false;            
    }
    
